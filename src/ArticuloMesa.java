
public class ArticuloMesa {
	private int mesa_numero,item_numero;
	private String hora, articulo_codigo,articulo_descripcion,observacion;
	private double cantidad, precio, total;
	
	
	public int getMesa_numero() {
		return mesa_numero;
	}
	public void setMesa_numero(int mesa_numero) {
		this.mesa_numero = mesa_numero;
	}
	public int getItem_numero() {
		return item_numero;
	}
	public void setItem_numero(int item_numero) {
		this.item_numero = item_numero;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public String getArticulo_codigo() {
		return articulo_codigo;
	}
	public void setArticulo_codigo(String articulo_codigo) {
		this.articulo_codigo = articulo_codigo;
	}
	public String getArticulo_descripcion() {
		return articulo_descripcion;
	}
	public void setArticulo_descripcion(String articulo_descripcion) {
		this.articulo_descripcion = articulo_descripcion;
	}
	public String getObservacion() {
		return observacion;
	}
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	public double getCantidad() {
		return cantidad;
	}
	public void setCantidad(double cantidad) {
		this.cantidad = cantidad;
	}
	public double getPrecio() {
		return precio;
	}
	public void setPrecio(double precio) {
		this.precio = precio;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	
	
	
}
